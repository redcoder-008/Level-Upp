public class Q2{
    public static void main(String[]args)
    {
    int n=4;
    for(int i=1;i<=n;i++)
    {
        //space
        for(int j=1;j<=n;j++)
        {
        System.out.print("_");
        
    }
    //star
    
    for(int k=1;k<=n;k++)
    {
        System.out.print("*");
    }
    //space
    
    System.out.println();
        
    }
    }
}